# https://py.checkio.org/mission/open-labyrinth/
import bisect
import math

STEPS = {(-1, 0): 'N', (1, 0): 'S', (0, -1): 'W', (0, 1): 'E'}


class Node:
    def __init__(self, x, y, walkable):
        self.x = x
        self.y = y
        self.walkable = walkable
        
        self.g = 0
        self.f = 0
        self.h = 0
        
        self.opened = False
        self.closed = False
        self.parent = None
    
    def __gt__(self, other):
        return self.f > other.f
    
    def __lt__(self, other):
        return self.f < other.f


class Grid:
    def __init__(self, matrix):
        self.width = len(matrix[0])
        self.height = len(matrix)
        
        self.nodes = self._build_nodes(matrix)
    
    @staticmethod
    def _build_nodes(matrix):
        nodes = []
        
        for y, row in enumerate(matrix):
            nodes.append([Node(x, y, not w) for x, w in enumerate(row)])
        
        return nodes
    
    @staticmethod
    def backtrace(node):
        path = ''
        
        while node.parent:
            index = (node.y - node.parent.y, node.x - node.parent.x)
            
            path = STEPS[index] + path
            node = node.parent
        
        return path
    
    def get_node(self, x, y):
        return self.nodes[y][x]
    
    def is_inside(self, x, y):
        return 0 <= x < self.width and 0 <= y < self.height
    
    def is_walkable(self, x, y):
        node = self.get_node(x, y)
        
        return self.is_inside(x, y) and node.walkable and not node.closed
    
    def get_neighbors(self, node):
        x = node.x
        y = node.y
        neighbors = []
        
        for i, j in [(x + 1, y), (x, y + 1), (x, y - 1), (x - 1, y)]:
            if self.is_walkable(i, j):
                neighbors.append(self.get_node(i, j))
        
        return neighbors


class Queue:
    def __init__(self):
        self.queue = []
    
    def push(self, node):
        bisect.insort_left(self.queue, node)
        node.opened = True
    
    def pop(self):
        if not self.empty():
            node = self.queue.pop(0)
            node.closed = True
            
            return node
    
    def empty(self):
        return not self.queue


def heuristic(x, y):
    return max(x, y) + math.sqrt(x * x + y * y)


def astar(start_x, start_y, end_x, end_y, matrix):
    grid = Grid(matrix)
    open_list = Queue()
    
    start_node = grid.get_node(start_x, start_y)
    end_node = grid.get_node(end_x, end_y)
    
    open_list.push(start_node)
    
    while not open_list.empty():
        node = open_list.pop()
        
        if node == end_node:
            return grid.backtrace(node)
        
        neighbors = grid.get_neighbors(node)
        
        for neighbor in neighbors:
            ng = node.g + 1
            
            if not neighbor.opened or ng < neighbor.g:
                neighbor.g = ng
                neighbor.h = neighbor.h or heuristic(abs(neighbor.x - end_node.x), abs(neighbor.y - end_node.y))
                neighbor.f = neighbor.g + neighbor.h
                
                neighbor.parent = node
                
                if not neighbor.opened:
                    open_list.push(neighbor)


def checkio(maze_map):
    return astar(1, 1, 10, 10, maze_map)


